struct ListNode {
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};

ListNode* reverseList(ListNode* head) {
    ListNode* curr = head;
    ListNode* last = NULL;
    while(curr!=NULL){
        ListNode* next = curr->next;
        curr->next = last;
        last = curr;
        curr = next;
    }
    return last;
}

